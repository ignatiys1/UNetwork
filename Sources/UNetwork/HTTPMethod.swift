public enum HTTPMethod: String {
    case get = "GET"
    
    case push = "PUSH"
    case put = "PUT"
    case post = "POST"

    case delete  = "DELETE"
    case patch  = "PATCH"
    case head  = "HEAD"
    case options  = "OPTIONS"
    case trace  = "TRACE"
}
